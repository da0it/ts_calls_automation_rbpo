package services

import "fmt"

func ticketSummarySystemPrompt() string {
	return `Ты формируешь стандартизированное описание тикета по транскрипции телефонного звонка.
Не генерируй заголовок, тип обращения, приоритет, теги или пояснения вне JSON.
Верни только JSON-объект без markdown и без дополнительного текста.

Формат ответа:
{
  "problem": "1 короткое предложение с сутью проблемы",
  "context": "1 короткое предложение с важным контекстом",
  "action": "1 короткое предложение с рекомендуемым следующим действием"
}

Правила:
- Пиши по-русски.
- Каждое поле: максимум одно короткое предложение.
- Не выдумывай факты, которых нет в транскрипции.
- Не повторяй intent, это поле уже известно системе.
- Если данных для поля недостаточно, верни пустую строку.
- Не включай персональные данные без необходимости.`
}

func buildSummaryUserPrompt(transcript, intentID, priority, entitiesInfo string) string {
	return fmt.Sprintf(`Транскрипция звонка:
%s

Тип обращения (не дублируй в ответе): %s
Приоритет: %s

Извлечённые сущности:
%s

Сформируй стандартизированное описание по заданному JSON-формату.`, transcript, intentID, priority, entitiesInfo)
}
